public class Exercicio3 {

	public static void main(String[] args) {
		
		Integer cont = new Integer(0);
		
		System.out.println("Os multiplos de 3 de 0 até 100 são: ");
		while ( cont < 101) {
			 
			if ( cont % 3 == 0 ) {
				 
				 System.out.print( cont + ", " );
				 
				 cont = cont + 1;
			 }
			 else 
				 

				 cont = cont + 1;
		
		}
	}

}
