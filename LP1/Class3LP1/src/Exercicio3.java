
public class Exercicio3 {

	public static void main(String[] args) {
	
		Integer i = new Integer(1);
		
		System.out.println("Os multiplos de 3, entre 1 e 100 são:");
		
		while (i <= 100) {
			
		    if (i % 3 == 0) {
		    	
		    	System.out.println(+i);
			
		    	i = i + 1; }
		    
		    else 
		    	
		    	i = i + 1;
		    	
		}

	}

}
